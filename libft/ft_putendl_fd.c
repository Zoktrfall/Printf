#include "libft.h"

/*
Выводит строку 's' в указанный файловый дескриптор
с последующей новой строкой.
*/

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write (fd, &s[i], 1);
			i++;
		}
		write (fd, "\n", 1);
	}
	return ;
}
